// 什么是组件化开发?
// 组件化开发指的是: 根据封装的思想, 把页面上可重用的 UI 结构封装为组件, 从而方便项目的开发和维护.

// vue 中的组件化开发：
// vue 是一个支持组件化开发的前端框架。
// vue 中规定：组建的后缀名是 .vue 。之前接触到的 App.vue 文件本质上就是一个 vue 的组件。

// vue 组件的三个组成部分:
// 每个 .vue 组件都由 3 部分构成，分别是:
// 1. template -> 组建的模板结构；
// 2. script -> 组建的 JavScript 行为；
// 3. style -> 组建的样式;

// 组件封装者 和 组件使用者
// 组件的模板  -->  组件的实例对象