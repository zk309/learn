// 声明式导航传参:
// navigator 组件的 url 属性用来指定将要跳转到的页面路径. 同时, 路径的后面还可以携带参数.
// 注意: 1. 参数与路径之间使用 ? 分隔;
//       2. 参数键与参数值用 = 分隔;
//       3. 不同参数用 & 分隔;

/****
 *<navigator url="/pages/logs/logs?user=刘一&age=12" open-type="navigate">
    <button type="warn">跳转到logo页面 (非 tabBer 页面)</button>
  </navigator>
 */