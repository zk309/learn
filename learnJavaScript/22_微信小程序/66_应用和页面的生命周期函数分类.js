// 应用的生命周期函数: 目前是 4 个
// 小程序的应用生命周期函数需要在 app.js 中进行声明.示例如下:
/****
 * app.js 文件
 * App({
 *    onLaunch: function() { },        小程序初始化完成时, 执行此函数, 全局只触发一次, 可以做一些初始化的工作:
 *    onShow: function (options) { },  当小程序启动，或从后台进入前台显示，会触发 onShow:
 *    onHide: function () { },         当小程序从前台进入后台，会触发 onHide
 *    onError: function (msg) { }      当小程序发生脚本错误，或者 api 调用失败时，会触发 onError 并带上错误信息
 * })
 */

// 页面的生命周期:
/****
 * 页面的 .js 文件
 * Page({
 *    onLoad: function(options) { },  监听页面加载, 一个页面只调用 1 次
 *    onReady: function() { },        监听页面初次渲染, 一个页面只调用 1 次
 *    onShow: function() { },         监听页面显示
 *    onHide: function() { },         监听页面隐藏
 *    onUnload: function() { },       监听页面卸载, 一个页面只调用 1 次
 * })
 */