// 常用的视图容器组件:
// 1. view 组件
// 普通视图区域;
// 类似于 HTML 中的 div, 是一个块级元素;
// 常用来实现页面的布局效果;

// 2. scroll-view 组件
// 可滚动的视图区域
// 常用来实现滚动列表效果;
// scroll-y 属性, 允许纵向滚动; 注意, 使用竖向滚动时, 需要给 scroll-view 一个固定高度.
// scroll-x 属性, 允许横向滚动;

// 3. swiper 和 swiper-item
//  轮播图容器组件 和 轮播图 item 组件;
//  相关的属性文档: https://developers.weixin.qq.com/miniprogram/dev/component/swiper.html