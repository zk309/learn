// 什么是 WXSS ?
// WXSS (Wei Style Sheets) 是一套样式语言, 用于美化 WXML 的组件样式, 类似于网页开发中的 CSS.

// WXSS 和 CSS 的关系
// WXSS 具有 CSS 大部分特性, 同时 WXSS 还对 CSS 进行了扩充以及修改, 以适应微信小程序的开发.
// 与 CSS 相比, WXSS 扩展的特性有:
// 1. rpx 尺寸单位;
// 2. @import 样式导入;